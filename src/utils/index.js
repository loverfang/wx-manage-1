/**
 * 构造树型结构数据
 * @param {*} data 数据源
 * @param {*} id id字段 默认 'id'
 * @param {*} parentId 父节点字段 默认 'parentId'
 * @param {*} children 孩子节点字段 默认 'children'
 * @param {*} rootId 根Id 默认 0
 */
export function handleTree(data, id, parentId, children, rootId) {
	id = id || 'id'
	parentId = parentId || 'parentId'
	children = children || 'children'
	rootId = rootId || Math.min.apply(Math, data.map(item => { return item[parentId] })) || 0
	//对源数据深度克隆
	const cloneData = JSON.parse(JSON.stringify(data))
	//循环所有项
	const treeData = cloneData.filter(father => {
		let branchArr = cloneData.filter(child => {
			//返回每一项的子级数组
			return father[id] === child[parentId]
		});
		branchArr.length > 0 ? father.children = branchArr : '';
		//返回第一层
		return father[parentId] === rootId;
	});
	return treeData != '' ? treeData : data;
}

/**
 * 树形数据转换
 * @param {*} data
 * @param {*} id
 * @param {*} pid
 */
export function treeDataTranslate(data, id = 'id', pid = 'parentId') {
  var res = []
  var temp = {}

  // 组成一个类似map的对象{0:{},1:{},{}}
  for (var i = 0; i < data.length; i++) {
      // data[i][id]传过来的第i个元素的id值
      temp[data[i][id]] = data[i]
  }
  // console.log(temp)

  for (var k = 0; k < data.length; k++) {
    // temp[data[k][pid]]在temp中找出父对象，
    // data[k][id]   //id对应的对象
    // data[k][pid]  //pid对应的父对象
    // 如果在temp找那个有对应的父对象，并且当前对象不等于父对象，那么就：
    if (temp[data[k][pid]] && data[k][id] !== data[k][pid]) {
      // 找到的父对象没有children的换创建一个空的children数组，
      if (!temp[data[k][pid]]['children']) {
        temp[data[k][pid]]['children'] = []
      }
      // 找到的父对象没有_level属性就给他设置并初始化为1
      if (!temp[data[k][pid]]['_level']) {
        temp[data[k][pid]]['_level'] = 1
      }
      // 为当前对象设置_level属性:值为它的父对象的_level+1;
      data[k]['_level'] = temp[data[k][pid]]._level + 1
      // 设置好_level后将当前对象放入到父对象的children数组中
      temp[data[k][pid]]['children'].push(data[k])
    } else {
      // 如果没有找到对应的父对象，按就直接放入到返回对象中当做一级对象处理
      // console.log('直接push')
      // console.log(data[k])
      res.push(data[k])
    }
  }

  console.log( '转换后的树形数据' )
  console.log( res )
  return res
}